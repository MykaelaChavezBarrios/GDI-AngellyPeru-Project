-- creación de la base de datos 
-- DROP DATABASE AngellyPeru_Fact_Bol
CREATE DATABASE IF NOT EXISTS AngellyPeru_Fact_Bol;
-- creación de tablas
USE AngellyPeru_Fact_Bol;
-- tabla artículo
CREATE TABLE IF NOT EXISTS Articulo (
	Codigo_Articulo VARCHAR(12) PRIMARY KEY,
	Descripcion VARCHAR(100) UNIQUE NOT NULL,
    Precio_unitario FLOAT NOT NULL,
    Empaque INT NOT NULL,
    Medida VARCHAR(6) NOT NULL,
    Stock INT NOT NULL 
) ENGINE = InnoDB;

-- tabla cliente natural
CREATE TABLE IF NOT EXISTS Cliente_Natural(
	DNI CHAR(8) PRIMARY KEY,
    Nombre VARCHAR(20) NOT NULL,
    Apellido VARCHAR(20) NOT NULL,
    Direccion VARCHAR(30)
)ENGINE = InnoDB;
CREATE INDEX Apellido_natural ON CLiente_Natural(Apellido);

-- tabla cliente juridico
CREATE TABLE IF NOT EXISTS Cliente_Juridico (
	RUC CHAR(11) PRIMARY KEY,
	Razon_Social VARCHAR(80) UNIQUE NOT NULL,
    Direccion_Fiscal VARCHAR(150)
)ENGINE = InnoDB;

-- tabla boleta
CREATE TABLE IF NOT EXISTS Boleta(
	Num_Boleta CHAR(7) PRIMARY KEY,
    Dia_B CHAR(2) NOT NULL,
    Mes_B CHAR(2) NOT NULL,
    Año_B CHAR(4) NOT NULL,
    ICBPER_B FLOAT,
    OP_Gravadas_B FLOAT NOT NULL,
    IGV_Total_B FLOAT NOT NULL,
    Total_B FLOAT NOT NULL,
    DNI CHAR(8) NOT NULL,
    CONSTRAINT FOREIGN KEY (DNI) REFERENCES Cliente_Natural(DNI) ON UPDATE CASCADE
)ENGINE = InnoDB;

-- tabla detalle boleta
CREATE TABLE IF NOT EXISTS Detalle_Boleta(
	Num_Boleta CHAR(7) NOT NULL,
    	Codigo_Articulo VARCHAR(12) NOT NULL,
	Cantidad_B INT NOT NULL,	
    	Descuento_B FLOAT,
    	Valor_Venta_B FLOAT  NOT NULL,
		PRIMARY KEY (Num_Boleta, Codigo_Articulo),
    	CONSTRAINT FOREIGN KEY (Num_Boleta) REFERENCES Boleta(Num_Boleta) ON UPDATE CASCADE,
    	CONSTRAINT FOREIGN KEY (Codigo_Articulo) REFERENCES Articulo(Codigo_Articulo) ON UPDATE CASCADE
)ENGINE = InnoDB;
CREATE INDEX DNI ON Boleta(DNI);

-- tabla factura
CREATE TABLE IF NOT EXISTS Factura(
	Num_Factura CHAR(7) PRIMARY KEY,
    Dia_F CHAR(2) NOT NULL,
    Mes_F CHAR(2) NOT NULL,
    Año_F CHAR(4) NOT NULL,
    ICBPER_F FLOAT,
    OP_Gravadas_F FLOAT NOT NULL,
    IGV_Total_F FLOAT NOT NULL,
    Total_F FLOAT NOT NULL,
    RUC CHAR(11) NOT NULL,
    CONSTRAINT FOREIGN KEY (RUC) REFERENCES Cliente_Juridico(RUC) ON UPDATE CASCADE
)ENGINE = InnoDB;
CREATE INDEX RUC ON Factura(RUC);

-- tabla detalle factura
CREATE TABLE IF NOT EXISTS Detalle_Factura(
	Num_Factura CHAR(7) NOT NULL,
    Codigo_Articulo VARCHAR(12) NOT NULL,
	Cantidad_F INT NOT NULL,
    Descuento_F FLOAT,
    Valor_Venta_F FLOAT NOT NULL,
	PRIMARY KEY (Num_Factura, Codigo_Articulo),
    CONSTRAINT FOREIGN KEY (Num_Factura) REFERENCES Factura(Num_Factura) ON UPDATE CASCADE,
   	CONSTRAINT FOREIGN KEY (Codigo_Articulo) REFERENCES Articulo(Codigo_Articulo) ON UPDATE CASCADE
)ENGINE = InnoDB;
